        public void almacenarArchivoObjetos(LinkedList<Paciente> losPacientes) 
    {
        // Actualizar el nombre del archivo
        String nombreArchivo = "Pacientes.obj";
            
        ObjectOutputStream  salida=null;
        try { 
            // escritura de datos
            salida  =  new  ObjectOutputStream( 
                    new  FileOutputStream( nombreArchivo ) ) ;
            
            // adicionar instruccion para escribir los datos
            salida.writeObject(losPacientes);
                         
        } catch( Exception  e ) { 
            System.out.println("Error almacenando los datos "+e.getMessage()) ;
        } finally
        {
            try{
                salida.close();
            } catch(Exception e){
               System.out.println("Error cerrando ") ;             
            }
        }                      
    }
    
    
    
