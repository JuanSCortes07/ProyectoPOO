    public final void recuperarArchivoObjetos(LinkedList<Paciente> losPacientes) 
    {
        
        // Actualizar el nombre del archivo
        String nombreArchivo = "Pacientes.obj";
        LinkedList<Paciente> laBD=new LinkedList();
        ObjectInputStream  entrada=null;
        try{
            // lectura de datos
            entrada  =   new  ObjectInputStream( 
                    new  FileInputStream( nombreArchivo ) ) ;

            // leer flujo y almacenarlo en laBD. Usar casting  
            laBD = (LinkedList<Paciente>) entrada.readObject();
            // realizar ciclo for_each para pasar los datos de laBD a losVehiculos
            for(Paciente obj : laBD)
            {
                losPacientes.add(obj);                
            }  
           
                           
        } catch( Exception  e ) { 
            JOptionPane.showMessageDialog(null,"Error recuperando los datos "+e.getMessage()+" -- "+e.toString()) ;
        } finally
        {
            try{
                entrada.close();
            } catch(Exception e){
               JOptionPane.showMessageDialog(null,"Error cerrando ") ;             
            }
        }                              
    } 

